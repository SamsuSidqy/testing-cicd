<?php

use Illuminate\Foundation\Application;
use Illuminate\Foundation\Configuration\Exceptions;
use Illuminate\Foundation\Configuration\Middleware;
use Illuminate\Http\Request;

return Application::configure(basePath: dirname(__DIR__))
    ->withRouting(
        web: __DIR__.'/../routes/web.php',
        commands: __DIR__.'/../routes/console.php',
        channels: __DIR__.'/../routes/channels.php',
        health: '/up',
    )
    ->withMiddleware(function (Middleware $middleware): void {
        //
    })
    ->withExceptions(function (Exceptions $exceptions): void {
        $exceptions->report(function (Exception $e){
            $fileLine = 'File : '. $e->getFile() . '| Line : ' . $e->getLine();
            $message = 'Kesalahan Dalam Server |' . $e->getMessage() . '|' . $fileLine;
            $statusCode = $e->getCode();
            try{
                App\Helpers\Developer\LoggingHelpers::CreateLogging('Error',$message,'Error',$statusCode);
            }catch(\Exception $err){
                 App\Helpers\Developer\LoggingHelpers::CreateLogging('Error',$err->getMessage(),'Error',$err->getCode());
            }
        });
    })->create();
